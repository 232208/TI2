#include <stdio.h>
#include <math.h>

int dodawanie(int a, int b)
{
	int temp = 0;
	temp = a + b;

	return temp;

}

int odejmowanie(int a, int b)
{
	int temp = 0;
	temp = a - b;

	return temp;
}

int mnozenie(int a, int b)
{
	int temp = 0;
	temp = a * b;

	return temp;
}

int dzielenie(int a, int b)
{
	int temp = 0;
	temp = a / b;

	return temp;

}

